def reduce_puzzle(values):
    """Reduce a Sudoku puzzle by repeatedly applying all constraint strategies

    Parameters
    ----------
    values(dict)
        a dictionary of the form {'box_name': '123456789', ...}

    Returns
    -------
    dict or False
        The values dictionary after continued application of the constraint strategies
        no longer produces any changes, or False if the puzzle is unsolvable 
    """
    # TODO: Copy your code from the classroom and modify it to complete this function
    stalled = False
    while not stalled:
        # Check how many boxes have a determined value
        solved_values_before = len([box for box in values.keys() if len(values[box]) == 1])

        # Apply the constraint strategies
        values = eliminate(values)
        values = only_choice(values)
        values = naked_twins(values)

        # Check how many boxes have a determined value, after applying the strategies
        solved_values_after = len([box for box in values.keys() if len(values[box]) == 1])

        # If no new values were determined, stop the loop
        stalled = solved_values_before == solved_values_after

        # If any box has no possible values, the puzzle is unsolvable
        if any(len(values[box]) == 0 for box in values.keys()):
            return False

    return values
    raise NotImplementedError


def search(values):
    """Apply depth first search to solve Sudoku puzzles in order to solve puzzles
    that cannot be solved by repeated reduction alone.

    Parameters
    ----------
    values(dict)
        a dictionary of the form {'box_name': '123456789', ...}

    Returns
    -------
    dict or False
        The values dictionary with all boxes assigned or False

    Notes
    -----
    You should be able to complete this function by copying your code from the classroom
    and extending it to call the naked twins strategy.
    """
    # TODO: Copy your code from the classroom to complete this function
    if values is False:
        return False  # Failed earlier, cannot solve

    if all(len(values[box]) == 1 for box in boxes):
        return values  # Solved!

    # Choose an unfilled box with the fewest possibilities
    _, box = min((len(values[box]), box) for box in boxes if len(values[box]) > 1)

    # Try each possibility recursively
    for digit in values[box]:
        new_values = values.copy()
        new_values[box] = digit
        result = search(new_values)
        if result:
            return result

    return False  # No solution found
    raise NotImplementedError


def solve(grid):
    """Find the solution to a Sudoku puzzle using search and constraint propagation

    Parameters
    ----------
    grid(string)
        a string representing a sudoku grid.
        
        Ex. '2.............62....1....7...6..8...3...9...7...6..4...4....8....52.............3'

    Returns
    -------
    dict or False
        The dictionary representation of the final sudoku grid or False if no solution exists.
    """
    values = grid2values(grid)
    values = search(values)
    return values


